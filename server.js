require('dotenv').config();
const express = require('express');
const mongoose = require('mongoose');
const http = require('http');
const socketIO = require('socket.io');
const cors = require('cors');
const path = require('path');

const app = express();
const server = http.createServer(app);
const io = socketIO(server, {
  cors: {
    origin: "*",
    methods: ["GET", "POST"]
  }
});

// Middleware
app.use(cors());
app.use(express.json());
app.use(express.urlencoded({ extended: true }));

// Serve static files from public directory
app.use(express.static(path.join(__dirname, 'public')));

// MongoDB Connection
const MONGODB_URI = process.env.MONGODB_URI || 'mongodb://localhost:27017/incident-reporting';

mongoose.connect(MONGODB_URI)
.then(() => console.log('âœ… Connected to MongoDB'))
.catch(err => {
  console.error('âŒ MongoDB connection error:', err);
  console.log('ğŸ’¡ Make sure MongoDB is running or use MongoDB Atlas cloud database');
});

// Import routes
const incidentRoutes = require('./routes/incidents');

// Make io accessible to routes
app.set('socketio', io);

// Routes
app.use('/api/incidents', incidentRoutes);

// Root route - serve index.html
app.get('/', (req, res) => {
  res.sendFile(path.join(__dirname, 'public', 'index.html'));
});

// Socket.IO connection handling
io.on('connection', (socket) => {
  console.log('ğŸ”Œ New client connected:', socket.id);
  
  socket.on('disconnect', () => {
    console.log('ğŸ”Œ Client disconnected:', socket.id);
  });
});

// Error handling middleware
app.use((err, req, res, next) => {
  console.error('Error:', err);
  res.status(500).json({ 
    error: 'Internal server error', 
    message: err.message 
  });
});

// Start server
const PORT = process.env.PORT || 3001;
server.listen(PORT, () => {
  console.log(`ğŸš€ Server running on http://localhost:${PORT}`);
  console.log(`ğŸ“ Incident Reporting Map is ready!`);
});
